package com.design_mode.creational_patterns.factory_pattern;

/**
 *  1.1 工厂模式
 *
 *    定义：是 Java 中最常用的设计模式之一。这种类型的设计模式属于创建型模式，它提供了一种创建对象的最佳方式。
 *    在工厂模式中，我们在创建对象时不会对客户端暴露创建逻辑，并且是通过使用一个共同的接口来指向新创建的对象。
 *
 *    简单工厂:是一种编程习惯，不能称为设计模式。
 *    工厂方法:
 *        +静态工厂方法
 *        +实例工厂方法
 *
 *   关键代码：创建过程在其子类执行。
 *
 */